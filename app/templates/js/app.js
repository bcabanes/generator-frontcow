var app = (function() {

    'use strict';
    var privateVariable = 'app fired!',
        docElem = document.documentElement;

    return {
        publicFunction: function() {
            console.log(privateVariable);
        },
        userAgentInit: function() {
            docElem.setAttribute('data-useragent', navigator.userAgent);
        }
    };

})();

(function() {

    'use strict';

    //foundation init
    $(document).foundation();

    // Some examples
    app.publicFunction();
    app.userAgentInit();

})();
